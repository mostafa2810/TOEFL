import com.google.android.gms.ads.MobileAds

class Main9Activity : AppCompatActivity() {
    private lateinit var mInterstitialAd: InterstitialAd
    override fun onCreate(savedInstanceState: Bundle?) {
        super.onCreate(savedInstanceState)
        setContentView(R.layout.activity_main9)
        MobileAds.initialize(this,"ca-app-pub-6572711285388576~1324320260")
        mInterstitialAd = InterstitialAd(this)
        mInterstitialAd.adUnitId = "ca-app-pub-6572711285388576/5370542338"
        mInterstitialAd.loadAd(AdRequest.Builder().build())
        val myWebView9: WebView =findViewById(R.id.myURL9)
        myWebView9.webViewClient=object : WebViewClient(){
            override fun shouldOverrideUrlLoading(view: WebView?, url: String?): Boolean {
                view?.loadUrl(url)
                return true
            }
        }
        myWebView9.settings.javaScriptEnabled=true
        myWebView9.settings.allowContentAccess=true
        myWebView9.settings.domStorageEnabled=true
        myWebView9.settings.useWideViewPort=true
        myWebView9.settings.setAppCacheEnabled(true)

        myWebView9.loadUrl("https://drive.google.com/file/d/1eJQnrYkIkdDRYce82hUQDF_dbqz9FQWo/view")


    }
    override fun onBackPressed() {
        mInterstitialAd.adListener = object: AdListener() {
            override fun onAdClosed() {
                // Code to be executed when the interstitial ad is closed.
                mInterstitialAd.loadAd(AdRequest.Builder().build())

            }
        }
        if (mInterstitialAd.isLoaded) {
            mInterstitialAd.show()
        } else {
            Log.d("TAG", "The interstitial wasn't loaded yet.")

        }
        super.onBackPressed()
    }
}